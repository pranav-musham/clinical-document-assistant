/**
 * Copy text to clipboard
 */
export async function copyToClipboard(text: string): Promise<void> {
  try {
    await navigator.clipboard.writeText(text)
  } catch (err) {
    // Fallback for older browsers
    const textArea = document.createElement('textarea')
    textArea.value = text
    textArea.style.position = 'fixed'
    textArea.style.left = '-999999px'
    document.body.appendChild(textArea)
    textArea.focus()
    textArea.select()
    try {
      document.execCommand('copy')
    } catch (err) {
      throw new Error('Failed to copy to clipboard')
    }
    document.body.removeChild(textArea)
  }
}

/**
 * Copy SOAP note to clipboard
 */
export async function copySOAPToClipboard(consultation: Consultation): Promise<void> {
  if (!consultation.soap_note) {
    throw new Error('No SOAP note available')
  }
  await copyToClipboard(consultation.soap_note)
}

/**
 * Copy transcript to clipboard
 */
export async function copyTranscriptToClipboard(consultation: Consultation): Promise<void> {
  if (!consultation.transcript) {
    throw new Error('No transcript available')
  }
  await copyToClipboard(consultation.transcript)
}

/**
 * Print consultation (opens browser print dialog)
 */
export function printConsultation(consultation: Consultation): void {
  const content = formatConsultationForExport(consultation)

  // Create a new window for printing
  const printWindow = window.open('', '_blank')
  if (!printWindow) {
    throw new Error('Could not open print window. Please check your popup blocker settings.')
  }

  printWindow.document.write(`
    <!DOCTYPE html>
    <html>
    <head>
      <title>Consultation #${consultation.id}</title>
      <style>
        body {
          font-family: 'Courier New', monospace;
          padding: 20px;
          max-width: 800px;
          margin: 0 auto;
          line-height: 1.6;
        }
        h1 {
          font-size: 18px;
          margin-bottom: 10px;
        }
        pre {
          white-space: pre-wrap;
          word-wrap: break-word;
          font-family: 'Courier New', monospace;
          font-size: 12px;
        }
        @media print {
          body {
            padding: 0;
          }
        }
      </style>
    </head>
    <body>
      <pre>${content}</pre>
    </body>
    </html>
  `)

  printWindow.document.close()
  printWindow.focus()

  // Wait for content to load before printing
  setTimeout(() => {
    printWindow.print()
  }, 250)
}

/**
 * Generate HTML for email
 */
export function generateEmailHTML(consultation: Consultation): string {
  const content = formatConsultationForExport(consultation)
  return `
    <html>
    <head>
      <style>
        body { font-family: Arial, sans-serif; line-height: 1.6; }
        pre { white-space: pre-wrap; word-wrap: break-word; background: #f4f4f4; padding: 10px; border-radius: 4px; }
      </style>
    </head>
    <body>
      <pre>${content}</pre>
    </body>
    </html>
  `
}

/**
 * Open email client with consultation data
 */
export function emailConsultation(consultation: Consultation, recipientEmail: string = ''): void {
  const subject = `Consultation #${consultation.id} - ${new Date(consultation.created_at).toLocaleDateString()}`
  const body = formatConsultationForExport(consultation)

  const mailtoLink = `mailto:${recipientEmail}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`
  window.location.href = mailtoLink
}
